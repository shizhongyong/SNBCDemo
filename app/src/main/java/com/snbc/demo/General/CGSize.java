package com.snbc.demo.General;

public class CGSize {
	float width;
	float height;
	public CGSize(int width, int height) {
		this.width = width ;
		this.height = height;
	}
	public float getWidth() {
		return width;
	}
	public void setWidth(float width) {
		this.width = width;
	}
	public float getHeight() {
		return height;
	}
	public void setHeight(float height) {
		this.height = height;
	}
}
